package com.diome.util;

public class Celular {

	public void TurnOn() {
		System.out.println("Iniciando sistema...");
	}
	public void AcceptCall() {
		System.out.println("Chamada em curso...");
	}
	public void RecuseCall() {
		System.out.println("Chamada recusada.");
	}
	public void BoxMail() {
		System.out.println("Chamada encaminhada para caixa postal, voce tem um novo recado");
	}
	public void DonotDisturbe() {
		System.out.println("Celular colocado em nao perturbe.");
	}
	public void TurnOff() {
		System.out.println("Desligando sistema...");
	}
}
